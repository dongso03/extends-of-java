public class Box {
	private int width;
	private int length;
	private int hei;

	public Box(int width, int length, int hei) {
		super();
		this.width = width;
		this.length = length;
		this.hei = hei;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

	public int getHei() {
		return hei;
	}

	public void setHei(int hei) {
		this.hei = hei;
	}

}
